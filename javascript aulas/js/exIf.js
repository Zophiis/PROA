let dia = parseInt(prompt("Digite o dia dew hoje: "))
if (dia = 1) {
    alert("Domingo.")
} else if (dia = 2) {
    alert("segunda")
} else if (dia = 3) {
    alert("Terça ")
} else if (dia = 4) {
    alert("Quarta")
} else if (dia = 5) {
    alert("Quinta")
} else if (dia = 6) {
    alert("Sexta")
} else if (dia = 7) {
    alert("Sábado")
} else
    alert("Não há dia correspondente")